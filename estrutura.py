def imprimirListaFazenda(self):
  for t in self.fazenda:
    t.print()

def  addFazendaLista(self,Fazenda):
  self.fazenda.append(Fazenda)


def  removeFazendaLista(self,Fazenda):
  self.fazenda.remove(Fazenda)
  

